package wesen;

public class Charakter extends Wesen{
	
	protected int staerke;
	protected int beweglichkeit;
	protected int intelligenz;
	
	
	public int getStaerke() {
		return staerke;
	}
	public void setStaerke(int staerke) {
		this.staerke = staerke;
	}
	public int getBeweglichkeit() {
		return beweglichkeit;
	}
	public void setBeweglichkeit(int beweglichkeit) {
		this.beweglichkeit = beweglichkeit;
	}
	public int getIntelligenz() {
		return intelligenz;
	}
	public void setIntelligenz(int intelligenz) {
		this.intelligenz = intelligenz;
	}
	
	

}
